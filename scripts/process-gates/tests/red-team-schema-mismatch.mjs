#!/usr/bin/env node
// Red-team test — Gate 2 calibration via deliberate schema mismatch.
//
// Plan v2.1 §"Soak test plan adds red-team exercise" requires this:
// during the 2-week soak, deliberately introduce a schema mismatch in a
// feature branch and verify Gate 2 (via Script 1) catches it. Tests the
// gate's calibration without waiting for an organic incident.
//
// What this script does:
//   1. Creates a temp file `workers/ai-tutor/routes/_red-team-canary.js`
//      with a db.prepare() call referencing a non-existent column.
//   2. Runs scripts/process-gates/precheck-worker-schema.mjs.
//   3. Asserts the artefact reports compatible: false AND lists the
//      fabricated column in mismatches.
//   4. Removes the temp file and re-runs Script 1, asserting compatible: true.
//   5. Reports pass/fail.
//
// The temp file is named with a leading underscore so it's clearly a test
// artefact; it's also gitignored at scripts/process-gates/tests/.gitignore
// to be doubly safe against accidental commits.
//
// Run: `node scripts/process-gates/tests/red-team-schema-mismatch.mjs`

import { existsSync, readFileSync, writeFileSync, unlinkSync, readdirSync, statSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, '..', '..', '..');
const CANARY_PATH = resolve(REPO_ROOT, 'workers/ai-tutor/routes/_red-team-canary.js');
const PRECHECK = resolve(REPO_ROOT, 'scripts/process-gates/precheck-worker-schema.mjs');
const ARTEFACT_DIR = resolve(REPO_ROOT, 'process-gate-artefacts');

const CANARY_CONTENTS = `// Red-team canary — DO NOT COMMIT.
// Generated by scripts/process-gates/tests/red-team-schema-mismatch.mjs.
// Contains a deliberate schema mismatch that Gate 2 / Script 1 must catch.
import { json } from '../helpers.js';
export async function _redTeamCanary(request, env) {
  const db = env.DB;
  // 'this_column_does_not_exist' is not in production schema. Script 1 must
  // flag this as a compatibility error.
  await db.prepare(
    'INSERT INTO accounts (id, email, name, this_column_does_not_exist) VALUES (?, ?, ?, ?)'
  ).bind('a', 'b', 'c', 'd').run();
  return json({ ok: false });
}
`;

function runPrecheck() {
  const result = spawnSync('node', [PRECHECK], {
    cwd: REPO_ROOT,
    encoding: 'utf8',
  });
  return { exitCode: result.status, stdout: result.stdout, stderr: result.stderr };
}

function readLatestPrecheck() {
  // Find the most recent wrangler-deploy-precheck-*.json
  if (!existsSync(ARTEFACT_DIR)) return null;
  const files = readdirSync(ARTEFACT_DIR)
    .filter(f => f.startsWith('wrangler-deploy-precheck-') && f.endsWith('.json'))
    .map(f => ({ f, mtime: statSync(resolve(ARTEFACT_DIR, f)).mtimeMs }))
    .sort((a, b) => b.mtime - a.mtime);
  if (!files.length) return null;
  const path = resolve(ARTEFACT_DIR, files[0].f);
  return { path, body: JSON.parse(readFileSync(path, 'utf8')) };
}

let passes = 0;
let fails = 0;

function assert(label, condition, detail = '') {
  if (condition) { process.stdout.write(`  PASS  ${label}\n`); passes++; }
  else { process.stdout.write(`  FAIL  ${label}\n  ${detail}\n`); fails++; }
}

async function main() {
  process.stdout.write('Red-team test — Gate 2 calibration\n');
  process.stdout.write('==================================\n');

  if (existsSync(CANARY_PATH)) {
    process.stderr.write('Canary file already exists. Aborting.\n');
    process.exit(2);
  }

  try {
    process.stdout.write('\n[1/4] Writing canary file...\n');
    writeFileSync(CANARY_PATH, CANARY_CONTENTS);

    process.stdout.write('[2/4] Running Script 1 against drifted Worker source...\n');
    const drifted = runPrecheck();
    const driftedArtefact = readLatestPrecheck();
    assert(
      'Script 1 exits non-zero on drift',
      drifted.exitCode !== 0,
      `exit=${drifted.exitCode}`
    );
    assert(
      'Drifted artefact reports compatible: false',
      driftedArtefact?.body?.script_output?.compatible === false,
      `compatible=${driftedArtefact?.body?.script_output?.compatible}`
    );
    assert(
      'Drifted artefact lists the fabricated column',
      (driftedArtefact?.body?.script_output?.mismatches || []).some(m =>
        /this_column_does_not_exist/.test(m.column || '') ||
        /this_column_does_not_exist/.test(m.reason || '')
      ),
      'mismatch entry not found'
    );

    process.stdout.write('\n[3/4] Removing canary file...\n');
    unlinkSync(CANARY_PATH);

    process.stdout.write('[4/4] Re-running Script 1 against clean Worker source...\n');
    const clean = runPrecheck();
    const cleanArtefact = readLatestPrecheck();
    assert(
      'Script 1 exits zero on clean Worker source',
      clean.exitCode === 0,
      `exit=${clean.exitCode}`
    );
    assert(
      'Clean artefact reports compatible: true',
      cleanArtefact?.body?.script_output?.compatible === true,
      `compatible=${cleanArtefact?.body?.script_output?.compatible}`
    );
  } finally {
    if (existsSync(CANARY_PATH)) {
      try { unlinkSync(CANARY_PATH); } catch {}
    }
  }

  process.stdout.write('\n==================================\n');
  process.stdout.write(`Total: ${passes + fails}  Pass: ${passes}  Fail: ${fails}\n`);
  if (fails > 0) {
    process.stdout.write('Gate 2 calibration is OFF. Investigate before relying on it.\n');
    process.exit(1);
  }
  process.stdout.write('Gate 2 catches schema-drift Workers and clears clean ones.\n');
}

main().catch(err => {
  console.error(err);
  if (existsSync(CANARY_PATH)) {
    try { unlinkSync(CANARY_PATH); } catch {}
  }
  process.exit(2);
});
